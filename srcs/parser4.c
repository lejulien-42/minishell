int
	manage_wc2(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0' && str[i] != '"' && str[i] != '\'')
	{
		if (str[i] == '\\' && str[i + 1] != '\0')
			i = i + 2;
		else
			i++;
	}
	return (i);
}
